package chapter1;

public class Calculator {

	public static void main(String[] args) {
		
	}

	static int add(int a , int b) {
		int result = a+b;
		
		return result;
	}

	static int sub(int a , int b) {
		int result = a-b;
			
		return result;
	}
	
	static int mul(int a , int b) {
		int result = a*b;
				
		return result;
	}
				
	static int div(int a , int b) {
		int result = a/b;
			
		return result;	
	}
	
}
